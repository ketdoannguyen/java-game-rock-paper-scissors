    public void run() {
        try {
            while (true) {
                try {
                    Thread.sleep(500);
                } catch (Exception ex) {
                }
                String line = inStream.readUTF();
                String z = "[0-9]{1}-----";
                if (line.matches(z)) {
                    enemy = Integer.parseInt(line.charAt(0) + "");
                    if (enemy == 1) {
                        jLabelBKBenemy.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/game/bua.png")));
                    } else if (enemy == 2) {
                        jLabelBKBenemy.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/game/keo.png")));
                    } else if (enemy == 3) {
                        jLabelBKBenemy.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/game/bao.png")));
                    }
                } else {
                    text.setText(text.getText() + "Enemy : " + line + "\n");
                }
            }
        } catch (IOException ex) {
        }
    }

