    void timeOut() {
        Thread thread = new Thread() {
            public void run() {
                while (true) {
                    try {
                        Thread.sleep(1000);
                    } catch (Exception ex) {
                    }
                    if (time.getText().equals("0")) {
                        Wrap();
                        send(tien + "-----");
                        try {
                            Thread.sleep(2000);
                        } catch (Exception ex) {
                        }
                        System.out.println("Enemy Client " + enemy);
                        ResultJDialog dialog = new ResultJDialog(Client.this, false);
                        dialog.setTitle("Client");
                        if ((tien == 3 && enemy == 1) || (tien == 2 && enemy == 3) || (tien == 1 && enemy == 2)) {
                            dialog.icon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/game/winJDialog.png")));
                            dialog.text.setText("Win");
                            jLabelGoalme.setText(Integer.parseInt(jLabelGoalme.getText()) + 1 + "");
                        } else if (tien == enemy) {
                            dialog.icon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/game/drawnJDialog.png")));
                            dialog.text.setText("Drawn");
                        } else {
                            dialog.icon.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/game/loseJDialog.png")));
                            dialog.text.setText("Lose");
                            jLabelGoalenemy.setText(Integer.parseInt(jLabelGoalenemy.getText()) + 1 + "");
                        }
                        dialog.setVisible(true);
                        try {
                            Thread.sleep(7000);
                            time.setText("15");
                            jLabelBKBme.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/game/question-mark.png")));
                            jLabelBKBenemy.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Image/game/question-mark.png")));
                            tien = 0;
                            //read();
                            time();
                            timeRandom();
                            timeOut();
                        } catch (Exception ex) {
                        }
                        break;
                    }
                }
            }
        };
        thread.start();
    }

    void send(String a) {
        try {
            outStream = new DataOutputStream(socket.getOutputStream());
            outStream.writeUTF(a);
            outStream.flush();
            textSend.setText("");
        } catch (IOException ex) {
        }
    }
